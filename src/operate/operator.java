package operate;

public class operator {

	public static void main(String[] args) {

		
		//증감 연산자(단항 연산자)
		//++ --
		// ++ 피연산자의 값을 1 증가
		// -- 피연산자의 값을 1 감소
		
		int a = 10;
		a++;
		System.out.println(a); // 11
		a++;
		System.out.println(a); // 12
		
		int b = 20;
		++b;
		System.out.println(b); // 21
		++b;
		System.out.println(b); // 22
		
		int c = 30;
		c--;
		System.out.println(c); //29
		--c;
		System.out.println(c); //28
		
		
	}

}
